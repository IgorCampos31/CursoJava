package AulaIf;

public class FuncaoIf1 {

    public static void main(String[] args) {

        int num = 10;
        if (num >= 10){
            System.out.println("O numero é maior que 10");
        }
        else {
            System.out.println("O numero é menor que 10");

        }
        System.out.println("O numero é: " + num);
    }
}
